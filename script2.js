document.getElementById('country1').addEventListener('click', function() { 
    const nums1 = document.getElementById('nums1'); 
    if (nums1.style.display === 'none') { 
        nums1.style.display = 'block'; 
    } else { 
        nums1.style.display = 'none'; 
    } 
});

